/* Keywords
 * Keywords are pre-defined words
 * These can't be used as identifiers
 * ex) auto, break, case, char, const, continue, default, do, ... 
 * ex) data types : char, enum, double, int, float, ...
 * ex) if-else statements : continue, if, for, while, goto, do, break
 * ex) return
 *  
 * 'include' or 'define', 'main' is NOT keywords
 * #include or #define is for preprocessor
 * 'main' is identifier, not keywords
 */
